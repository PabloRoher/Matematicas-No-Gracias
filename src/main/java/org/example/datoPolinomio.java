package org.example;

public class datoPolinomio{
    int valor;
    int termino;

    public datoPolinomio(int valor, int termino) {
        this.valor = valor;
        this.termino = termino;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public int getTermino() {
        return termino;
    }

    public void setTermino(int termino) {
        this.termino = termino;
    }







}
